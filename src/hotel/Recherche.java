    private void jbRechercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbRechercherActionPerformed
        
        if(jcDestination.getSelectedIndex()==0)
        JOptionPane.showMessageDialog(this,"Selectionner destination","Selectionner destination",JOptionPane.ERROR_MESSAGE) ;

        else if((jdateDu.getDate()==null)||(jdateAu.getDate()==null))
        JOptionPane.showMessageDialog(this,"Choisir date","Choisir date",JOptionPane.ERROR_MESSAGE) ;
           
        else{
        String f= "yyyy-MM-dd";
        SimpleDateFormat dt = new SimpleDateFormat(f);
        String du,au;
        int x;
           du=dt.format(jdateDu.getDate());
           au=dt.format(jdateAu.getDate());
           x=(int) (jdateAu.getDate().getTime()-jdateDu.getDate().getTime()); 
            Resultat r=new Resultat(jcDestination.getSelectedItem().toString(),du,au,jcAdulte.getSelectedIndex()+jcEnfant.getSelectedIndex()+1, (int) TimeUnit.DAYS.convert(x, TimeUnit.MILLISECONDS),jcCategorie.getSelectedItem().toString(),jcPension.getSelectedIndex());
           
            r.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jbRechercherActionPerformed

