    private void jCalendarControle(){
        
	Calendar cal = Calendar.getInstance();
        jdateDu.getJCalendar().setMinSelectableDate(cal.getTime());
  
    }
     
    private void jCalendarDuListener(){
        jdateDu.getDateEditor().addPropertyChangeListener(new PropertyChangeListener() {
            @Override
            public void propertyChange(PropertyChangeEvent evt) {
                Calendar cal;
                cal = jdateDu.getCalendar();
                cal.add(Calendar.DATE,1);
              jdateAu.getJCalendar().setMinSelectableDate(cal.getTime());}});
    }
    
    private void jCalendarAuListener(){
        jdateAu.getDateEditor().addPropertyChangeListener(new PropertyChangeListener() {
            @Override
            public void propertyChange(PropertyChangeEvent evt) {
        String f= "yyyy-MM-dd";
        SimpleDateFormat dt = new SimpleDateFormat(f);
        String du,au;
        int x;
          du=dt.format(jdateDu.getDate());
           au=dt.format(jdateDu.getDate());
           x=(int)(jdateAu.getDate().getTime()-jdateDu.getDate().getTime()); 
              jlNbNuit.setText(""+(int) TimeUnit.DAYS.convert(x, TimeUnit.MILLISECONDS));
        
    }
        });}
        
   
    
